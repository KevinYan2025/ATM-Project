
public class Account {
	private int id=2001;
	private int pin=1230;
	public Account() {
	}
	public Account(int id,int pin) {
		this.id=id;
		this.pin=pin;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	
	

}
